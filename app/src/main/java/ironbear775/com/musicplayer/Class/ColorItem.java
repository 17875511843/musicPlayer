public class ColorItem {
    private int name;
    private int color;
    private boolean checked;

    public void setName(int name) {
        this.name = name;
    }

    public int getName() {
        return name;
    }

    public void setColor(int color) {
        this.color = color;
    }

    public int getColor() {
        return color;
    }

    public void setChecked(boolean checked) {
        this.checked = checked;
    }

    public Boolean getChecked(){
        return checked;
    }
}
