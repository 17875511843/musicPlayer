public class OpenSourceItem {
    private String name;
    private String detail;

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDetail() {
        return detail;
    }

    public String getName() {
        return name;
    }
}
